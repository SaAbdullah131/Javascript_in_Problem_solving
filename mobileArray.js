// Create a mobile array and print value of it using while loop.. 
var mobile=["Samsung","Iphone","Nokia","Xamoi","Lenevo","Oppo"];
var index=0;
while(index<mobile.length){
    console.log("Mobile is: ",mobile[index]);
    index++;
}


